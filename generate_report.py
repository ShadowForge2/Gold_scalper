class PDF(FPDF):
    def header(self):
        if self.page_no() > 1:
            self.set_font("Helvetica", "I", 8)
            self.set_text_color(120)
            self.cell(0, 5, "Quantora FX - MetaStrategy Backtest Report", 0, 0, "L")
            self.cell(0, 5, f"Page {self.page_no()}", 0, 1, "R")
            self.line(10, 13, 200, 13)
            self.ln(4)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "I", 7)
        self.set_text_color(150)
        self.cell(0, 10, "Confidential - Generated by OpenCode AI", 0, 0, "C")

    def chapter_title(self, title):
        self.set_font("Helvetica", "B", 14)
        self.set_text_color(25, 60, 120)
        self.cell(0, 10, title, 0, 1, "L")
        self.set_draw_color(25, 60, 120)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(4)

    def sub_title(self, title):
        self.set_font("Helvetica", "B", 11)
        self.set_text_color(50, 80, 140)
        self.cell(0, 8, title, 0, 1, "L")
        self.ln(1)

    def body_text(self, text):
        self.set_font("Helvetica", "", 10)
        self.set_text_color(30, 30, 30)
        self.multi_cell(0, 5.5, text)
        self.ln(2)

    def bullet(self, text):
        self.set_font("Helvetica", "", 10)
        self.set_text_color(30, 30, 30)
        x = self.get_x()
        self.cell(6, 5.5, "-", 0, 0)
        self.multi_cell(0, 5.5, text)
        self.ln(1)

    def key_value(self, key, value):
        self.set_font("Helvetica", "B", 10)
        self.set_text_color(60, 60, 60)
        self.cell(60, 5.5, key)
        self.set_font("Helvetica", "", 10)
        self.set_text_color(30, 30, 30)
        self.cell(0, 5.5, value, 0, 1)
        self.ln(0.5)

    def add_table(self, headers, data, col_widths=None):
        if col_widths is None:
            w = 190 / len(headers)
            col_widths = [w] * len(headers)
        self.set_font("Helvetica", "B", 8)
        self.set_fill_color(25, 60, 120)
        self.set_text_color(255)
        for i, h in enumerate(headers):
            self.cell(col_widths[i], 7, h, 1, 0, "C", 1)
        self.ln()
        self.set_font("Courier", "", 7.5)
        self.set_text_color(30, 30, 30)
        fill = False
        for row in data:
            if fill:
                self.set_fill_color(235, 240, 250)
            else:
                self.set_fill_color(255)
            for i, cell in enumerate(row):
                align = "R" if i > 0 else "L"
                self.cell(col_widths[i], 6, str(cell), 1, 0, align, 1)
            self.ln()
            fill = not fill

